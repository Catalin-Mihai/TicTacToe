package com.example.catalin.tictactoe;

public class ReturnIndexes {
    private int i;
    private int j;

    public ReturnIndexes(int x, int y)
    {
        this.i = x;
        this.j = y;
    }

    public int geti() {
        return this.i;
    }

    public int getj() {
        return this.j;
    }
}
